package org.exoplatform.ide.extension.samples.client.inviting.manage;

/**
 * @author <a href="mailto:vzhukovskii@exoplatform.com">Vladislav Zhukovskii</a>
 * @version $Id: $
 */
public class UserInvitations {
    private String id;
    private String recipient;
    private String status;

    public UserInvitations(String id, String recipient, String status) {
        this.id = id;
        this.recipient = recipient;
        this.status = status;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRecipient() {
        return recipient;
    }

    public void setRecipient(String recipient) {
        this.recipient = recipient;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        UserInvitations that = (UserInvitations)o;

        if (!id.equals(that.id)) return false;
        if (!recipient.equals(that.recipient)) return false;
        if (status != null ? !status.equals(that.status) : that.status != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = id.hashCode();
        result = 31 * result + recipient.hashCode();
        result = 31 * result + (status != null ? status.hashCode() : 0);
        return result;
    }
}
